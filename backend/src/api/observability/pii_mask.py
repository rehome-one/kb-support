"""Маскирование ПДн в строках (NFR-1.5) — свой код (паттерн kb-platform
`chat/pii_masking.py`, БЕЗ shared-импорта, арх-константа).

Эвристики для текста логов: email / телефон РФ / паспорт / СНИЛС / ИНН → `***`.
Консервативно: лучше замаскировать спорное, чем оставить ПДн в plaintext.
"""

from __future__ import annotations

import re

# Порядок важен: специфичные паттерны (СНИЛС/паспорт) — до общего ИНН/числа.
_PATTERNS: tuple[re.Pattern[str], ...] = (
    # email
    re.compile(r"[\w.+-]+@[\w-]+\.[\w.-]+"),
    # телефон РФ: +7/8, опц. скобки/разделители
    re.compile(r"(?<!\d)(?:\+7|8)[\s\-()]*\d{3}[\s\-()]*\d{3}[\s\-]*\d{2}[\s\-]*\d{2}(?!\d)"),
    # СНИЛС: 123-456-789 01
    re.compile(r"(?<!\d)\d{3}-\d{3}-\d{3}[\s-]?\d{2}(?!\d)"),
    # паспорт РФ: серия (4) + номер (6), опц. пробел
    re.compile(r"(?<!\d)\d{4}\s?\d{6}(?!\d)"),
    # ИНН (10 или 12 цифр) и прочие длинные числовые ПДн
    re.compile(r"(?<!\d)\d{10,12}(?!\d)"),
)

_MASK = "***"


def mask_pii(text: str) -> str:
    """Заменить найденные ПДн в строке на `***`."""
    for pattern in _PATTERNS:
        text = pattern.sub(_MASK, text)
    return text
