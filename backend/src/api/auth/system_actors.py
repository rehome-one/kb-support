"""Реестр системных акторов аудита (решение Архитектора по #106, 2026-06-04).

`TicketHistory.actor_id` — NOT NULL (инвариант E1: «у каждой записи аудита есть
актор»). Изменения, инициированные не оператором, а системным процессом (движок
автоматизации, SLA-таймер и т.п.), фиксируются в неизменяемом журнале §3.7 под
**резервным sentinel-UUID** из диапазона `00000000-0000-0000-0000-0000000000XX`,
который Keycloak как random-v4 не выдаёт → коллизия с реальным user-id исключена.

Решение Архитектора (Вариант A, #106 issuecomment): именованный реестр, а не
nullable `actor_id` (схлопнул бы все системные источники в один NULL — у history
нет `author_type`, и это ослабило бы claims/legal-аудит) и не config-driven UUID
(расхождение аудита между средами). Инвариант NOT NULL сохранён, миграции нет.

Будущие системные акторы (SLA-таймер #90/#108, time_based #110, email-канал E7)
добавляются сюда же — единообразно. Для трассируемости конкретики (какое правило)
вызывающий кладёт доменный id (напр. `automation_rule_id`) в `to_value` записи.
"""

